<?php

namespace App\Jobs;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldBeUnique;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Foundation\Bus\Dispatchable;
use Illuminate\Queue\InteractsWithQueue;
use Illuminate\Queue\SerializesModels;
use App\Omure\Activities\WeatherForcastActivity;
use Illuminate\Support\Facades\Log;

class FetchWeatherForcast implements ShouldQueue, ShouldBeUnique
{
    use Dispatchable, InteractsWithQueue, Queueable, SerializesModels;

    /**
     * Create a new job instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Execute the job.
     *
     * @return void
     */
    public function handle(WeatherForcastActivity $weatherForcastActivity)
    {   
        Log::info('FetchWeatherForcast Job is running at ' . now());

        $weatherForcastActivity->fetchWeatherForcasts();
    }
}
